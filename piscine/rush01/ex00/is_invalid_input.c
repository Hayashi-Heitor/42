#include"rush01.h"

int	is_invalid_input(char *agrv)
{	
	int	i;

	i = 0;
	while (*agrv)
	{
		if (
			(i % 2 != 0 && *agrv != ' ')
			|| (i % 2 == 0 && (*agrv < '0' || *agrv > '9'))
		)
			return (1);
		i++;
		agrv++;
	}
	return (0);
}
